class LogLevel:
    """An "enumeration" used to control log levels and output"""

    DEBUG = 0
    """A detailed log with diagnostic information"""

    INFO = 1
    """A low severity log for informational purposes"""

    WARN = 2
    """A log indicating something unexpected happened that may or may not lead to other problems"""

    ERROR = 3
    """A log indicating a problem occurred and requires action"""
